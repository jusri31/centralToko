    private void btnHapusPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusPActionPerformed
        // TODO add your handling code here:
        if(pembelian !=null){
            int hps= JOptionPane.showConfirmDialog(Main.getFrame(),"Yakin hapus Data","CONFIRM",JOptionPane.OK_CANCEL_OPTION ,JOptionPane.QUESTION_MESSAGE, getFrameIcon());
               if(hps==0){ 
                Main.getTransaksiService().hapus(pembelian);
                pembelian=null;
                selectedPembelian=null;
                details.clear();
                enableDetaiForm(false);
                bersih();
               }
        }else{
            JOptionPane.showMessageDialog(Main.getFrame(), "Pilih Data lewat tombol cari", "WARNING", JOptionPane.WARNING_MESSAGE,getFrameIcon());
        }
        
    }//GEN-LAST:event_btnHapusPActionPerformed
    
    private class TabelDetaiModel extends AbstractTableModel{

        @Override
        public int getRowCount() {
            return details.size();
        }

        @Override
        public int getColumnCount() {
            return 4;
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            PembelianDetail p = details.get(rowIndex);
            switch(columnIndex){
                case 0 : return p.getProduk().getNama();
                case 1 : return p.getHarga();
                case 2 : return p.getJumlah();
                case 3 : return p.getSubTotal();
                default : return "";
            }
        }
        
    }
    
    private class TabelDetailListener implements ListSelectionListener{

        @Override
        public void valueChanged(ListSelectionEvent e) {
            if(tblPembelian.getSelectedRow()>=0){
                int index=tblPembelian.convertRowIndexToModel(tblPembelian.getSelectedRow());
                selectedPembelian=details.get(index);
            }
        }
        
    }
    
    private void calculate(){
        BigDecimal total=BigDecimal.ZERO;
        for(PembelianDetail p : details){
            total=total.add(p.getSubTotal());
        }
        txtTotal.setText(UtilityTools.formatNumber(total));
    }
    
    private void bersih(){
        txtID.setText("");
        txtTotal.setText("");
        txtTGL.setDate(new Date());
        details.clear();
        tblPembelian.setModel(new TabelDetaiModel());
    }
    
    private void enableDetaiForm(boolean status){
        btnTambahPD.setEnabled(status);
        btnEditPD.setEnabled(status);
        btnHapusPD.setEnabled(status);
        txtTotal.setEditable(false);
        txtTGL.setEnabled(false);
        
    }
    
//    Meload data dari doamain ke form
    private void loadDomainToForm(){
        txtID.setText(String.valueOf(pembelian.getId()));
        txtTGL.setDate(pembelian.getTgl_beli());
        txtTotal.setText(UtilityTools.formatNumber(pembelian.getTotal()));
        details=pembelian.getPembelianDetail();
        tblPembelian.setModel(new TabelDetaiModel());
    }
//    Meload data dari form ke model
    private void loadFormToModel(){
        pembelian.setTgl_beli(txtTGL.getDate());
        pembelian.setTotal(UtilityTools.parseNumberToBigDecimal(txtTotal.getText()));
        pembelian.setPembelianDetail(details);
       
    }
    

