    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if(penjualan==null) penjualan=new Penjualan();
        loadFormToModel();
        try{
            Main.getTransaksiService().simpan(penjualan);
        }catch(StaleObjectStateException ex){
            JOptionPane.showMessageDialog(Main.getFrame(), "Data sudah di Update user lain","WARNING" ,JOptionPane.WARNING_MESSAGE, getFrameIcon());
            log.error("Gagal simpan data penjualan", ex);
        }
        
        penjualan=null;
        bersih();
    }//GEN-LAST:event_btnSimpanActionPerformed

