    private void btnTambahPDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahPDActionPerformed
        // TODO add your handling code here:
        PembelianDetail p = new PembelianLooKDialog().showDialog();
        if(p !=null){
            details.add(p);
            
            tblPembelian.setModel(new TabelDetaiModel());
            calculate();
        }
    }//GEN-LAST:event_btnTambahPDActionPerformed

    private void btnEditPDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditPDActionPerformed
        // TODO add your handling code here:
        if(selectedPembelian !=null){
            selectedPembelian=new PembelianLooKDialog().showDialog(selectedPembelian);
            tblPembelian.tableChanged(new TableModelEvent(tblPembelian.getModel(), tblPembelian.getSelectedRow()));
            calculate();
        }else{
            JOptionPane.showMessageDialog(Main.getFrame(), "Pilih Pembelian Detail","WARNING", JOptionPane.WARNING_MESSAGE, getFrameIcon());
        }
    }//GEN-LAST:event_btnEditPDActionPerformed

    private void btnHapusPDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusPDActionPerformed
        // TODO add your handling code here:
        if(selectedPembelian !=null){
            details.remove(selectedPembelian);
            tblPembelian.setModel(new TabelDetaiModel());
            calculate();
            selectedPembelian=null;
        }else{
            JOptionPane.showMessageDialog(Main.getFrame(), "Pilih Pembelian Detail", "WARNING",JOptionPane.WARNING_MESSAGE, getFrameIcon());
        }
    }//GEN-LAST:event_btnHapusPDActionPerformed

