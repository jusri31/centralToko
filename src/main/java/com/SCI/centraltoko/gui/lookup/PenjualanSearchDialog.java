    private class TabelPenjualanModel extends AbstractTableModel{

        @Override
        public int getRowCount() {
            return penjualans.size();
        }

        @Override
        public int getColumnCount() {
            return 3;
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            Penjualan p= penjualans.get(rowIndex);
            
            switch(columnIndex){
                case 0 : return p.getId();
                case 1 : return p.getTglJual();
                case 2 : return p.getTotal();
                default : return "";
            }
        }
        
    }
    
    private class TabelPenjualanListener implements ListSelectionListener{

        @Override
        public void valueChanged(ListSelectionEvent e) {
            if(tblPenjualan.getSelectedRow()>=0){
                int index=tblPenjualan.convertRowIndexToModel(tblPenjualan.getSelectedRow());
                selectedPenjualan=penjualans.get(index);
            }
        }
        
    }
    
