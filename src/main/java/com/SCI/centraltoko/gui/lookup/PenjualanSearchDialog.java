    @SuppressWarnings("IndexOfReplaceableByContains")
    private void txtCariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCariKeyReleased
        // TODO add your handling code here:
        for(int i=0;i<tblPenjualan.getRowCount();i++){
            String current= tblPenjualan.getValueAt(i, 0).toString();
            if(current.toLowerCase().indexOf(txtCari.getText().toLowerCase())>=0){
                tblPenjualan.getSelectionModel().setSelectionInterval(i, i);
                tblPenjualan.getColumnModel().getSelectionModel().setSelectionInterval(0, 0);
                UtilityTools.scroolTorect(tblPenjualan, i);
                break;
            }
        }
    }//GEN-LAST:event_txtCariKeyReleased

