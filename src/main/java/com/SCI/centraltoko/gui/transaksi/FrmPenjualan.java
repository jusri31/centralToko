    private void btnTambahPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahPActionPerformed
        // TODO add your handling code here:
        PenjualanDetail p=new PenjualanAddDialog().showDialog();
        if(p!=null){
            listDetails.add(p);
            tblPenjualan.setModel(new TabelPenjualanModel());
            calculate();
        }
    }//GEN-LAST:event_btnTambahPActionPerformed

    private void btnEditPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditPActionPerformed
        // TODO add your handling code here:
        if(selectedPenjualan != null){
            selectedPenjualan=new PenjualanAddDialog().showDialog(selectedPenjualan);
            tblPenjualan.tableChanged(new TableModelEvent(tblPenjualan.getModel(), tblPenjualan.getSelectedRow()));
            calculate();
        }else{
            JOptionPane.showMessageDialog(Main.getFrame(),"Pilih penjualan", "WARNING",JOptionPane.WARNING_MESSAGE,getFrameIcon());
        }
    }//GEN-LAST:event_btnEditPActionPerformed

    private void btnHapusPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusPActionPerformed
        // TODO add your handling code here:
        if(selectedPenjualan !=null){
            listDetails.remove(selectedPenjualan);
            tblPenjualan.setModel(new TabelPenjualanModel());
            calculate();
            selectedPenjualan=null;
        }else{
            JOptionPane.showMessageDialog(Main.getFrame(),"Pilih penjualan","WARNING",JOptionPane.WARNING_MESSAGE, getFrameIcon());
        }
    }//GEN-LAST:event_btnHapusPActionPerformed

