    private void btnSimpanPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanPActionPerformed
        // TODO add your handling code here:
        if(pembelian ==null) pembelian=new Pembelian();
        
        loadFormToModel();
        try{
            Main.getTransaksiService().simpan(pembelian);
           
        }catch(StaleObjectStateException ex){
            JOptionPane.showMessageDialog(Main.getFrame(), "Data sudah di update user lain","WARNING", JOptionPane.WARNING_MESSAGE,getFrameIcon());
            log.error("Gagal simpan pembelian", ex);
        }
        pembelian=null;
        bersih();
        enableDetaiForm(false);
    }//GEN-LAST:event_btnSimpanPActionPerformed

